
# Crie um programa que imprima na tela os números de 1 a 10, em ordem crescente, usando um laço for e a função range().
for i in (range(1, 11, 1)):
    print(i, " ", end="")

# Obs: Se a gente não informar o valor inicial, por padrão, o range() inicia do zero. Se a gente não informar o valor do passo, por padrão, o range() incrementa de um em um.

