'''# 🐍 RESUMO – PALAVRAS RESERVADAS DO PYTHON

## 📌 O que são?

Palavras reservadas são termos que já possuem um significado específico no Python.
❗ Não podem ser usadas como nomes de variáveis.

---

## 🧠 1. Controle de Fluxo (Decisões)

Usadas para tomar decisões no código.

* `if` → se
* `else` → senão
* `elif` → senão se

📍 Exemplo:

```python
if idade >= 18:
    print("Maior de idade")
else:
    print("Menor de idade")
```

---

## 🔁 2. Estruturas de Repetição (Loops)

Permitem repetir ações.

* `for` → repetição com quantidade definida
* `while` → repete enquanto a condição for verdadeira

📍 Exemplo:

```python
for i in range(5):
    print(i)
```

---

## 🧩 3. Estrutura do Programa

Organizam o código.

* `def` → cria funções
* `return` → retorna um valor
* `class` → cria classes

📍 Exemplo:

```python
def saudacao():
    return "Olá!"
```

---

## 🔗 4. Operadores Lógicos

Usados para combinar condições.

* `and` → e
* `or` → ou
* `not` → negação

📍 Exemplo:

```python
if idade >= 18 and tem_carteira:
    print("Pode dirigir")
```

---

## 📦 5. Importação de Módulos

Permitem usar bibliotecas externas.

* `import` → importa módulo completo
* `from` → importa parte específica

📍 Exemplo:

```python
import math
```

---

## ⚠️ 6. Tratamento de Erros

Evita que o programa pare com falhas.

* `try` → tenta executar
* `except` → trata erro
* `finally` → executa sempre
* `raise` → lança erro

📍 Exemplo:

```python
try:
    x = int("abc")
except:
    print("Erro")
```

---

## 🧪 7. Valores Especiais

Valores prontos do Python.

* `True` → verdadeiro
* `False` → falso
* `None` → sem valor

📍 Exemplo:

```python
ativo = True
```

---

## 🎯 DICA IMPORTANTE

✔ Não é necessário decorar todas as palavras
✔ O mais importante é entender quando usar cada uma

---

## 🧠 RESUMO FINAL

* Decisão → `if`, `else`, `elif`
* Repetição → `for`, `while`
* Estrutura → `def`, `class`
* Lógica → `and`, `or`, `not`
* Importação → `import`, `from`
* Erros → `try`, `except`
* Valores → `True`, `False`, `None`

---

📌 Estude com prática! Programar é aprender fazendo.
'''